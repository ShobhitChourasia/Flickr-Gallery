import UIKit

extension UIColor {
    
    class func customBlueColor() -> UIColor{
        return UIColor(red:59/255, green:115/255, blue:181/255, alpha:1)
    }
    
    class func paleOrangeColor() -> UIColor{
        return UIColor(red:243/255, green:239/255, blue:127/255, alpha:0.8)
    }
    
}
